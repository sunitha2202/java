package com.example.myfirstpackage;

public class TypeConversion {

	public static void main(String[] args) {
		// type casting
		// implicit conversion
		int value = 35; // int is 32 bit
		long value1 = value; // automatically converted to long 64bits
		System.out.println("value1");
		float a = 6.4f;
		double b = a;
		System.out.println(b);

		// explicit conversion
		double x = 9.8;
		int y = (int) x;
		System.out.println(y);

		// object casting
		

	}

}
